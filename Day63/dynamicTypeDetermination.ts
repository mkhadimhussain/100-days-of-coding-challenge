// Dynamic Type Determination

// If you choose not to use type annotations in TypeScript, the language can still infer the type
// dynamically based on your declaration. For instance:

let greeting = "Hello, world!";
console.log(greeting);

// In this example, TypeScript will automatically infer the type of the variable greeting as a
// string. However, while TypeScript can infer types, it's generally not recommended to rely solely
// on dynamic type inference, as it may lead to unintended issues and reduced code clarity. Explicitly
// defining types through type annotations is a best practice for writing maintainable and error-free
// TypeScript code.
